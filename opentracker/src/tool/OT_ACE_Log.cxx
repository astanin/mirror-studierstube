#include <ace/Log_Msg.h>


void LOG_ACE_INFO(const char* nString, ...)
{
    char* str = new char[8192];

    va_list marker;
    va_start(marker, nString);
    vsprintf(str, nString, marker);

    ACE_DEBUG((LM_INFO, str ));
    delete [] str;
}


void LOG_ACE_ERROR(const char* nString, ...)
{
    char* str = new char[8192];

    va_list marker;
    va_start(marker, nString);
    vsprintf(str, nString, marker);

    ACE_DEBUG((LM_ERROR, str ));
    delete [] str;
}

